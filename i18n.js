function applyLanguage(lang) {
  const dict = translations[lang] || translations.en;
  const languageSelect = document.getElementById('language-select');

  document.documentElement.lang = lang;
  document.title = dict.title;
  if (languageSelect) {
    languageSelect.setAttribute('aria-label', dict["lang.label"] || "Language");
  }

  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      el.textContent = dict[key];
    }
  });

  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key]) {
      el.innerHTML = dict[key];
    }
  });
}

const storageKey = 'prefire-lang';
const select = document.getElementById('language-select');
const savedLang = localStorage.getItem(storageKey);
const initialLang = savedLang && translations[savedLang] ? savedLang : 'en';

select.value = initialLang;
applyLanguage(initialLang);

select.addEventListener('change', (event) => {
  const lang = event.target.value;
  localStorage.setItem(storageKey, lang);
  applyLanguage(lang);
});
